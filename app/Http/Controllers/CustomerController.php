    public function getProfile()
    {
        $customer = Customer::findOrFail(auth('customer')->id());
        $eta = '';
        if($customer->wedding)
        {
            $eta = (new Datetime($customer->wedding->date))->diff(new Datetime('now'));
        }
        return view('customers.dashboard')->withCustomer($customer)->withEta($eta); 
